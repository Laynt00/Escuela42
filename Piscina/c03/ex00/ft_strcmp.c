#include <string.h>
#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int		i;

	i = 0;
	while (s1[i])
	{
		if (s1[i] != s2[i])
		{
			return (s1[i] - s2[i]);
		}
		i++;
	}
	if (s2[i] == '\0')
		return (0);
	else
		return (-s2[i]);
}

int main(void)
{
	char *str1;
	str1 = "ho";
	char *str2;
	str2 = "hos";
	printf("%i", ft_strcmp(str1, str2));
	return (0);
}